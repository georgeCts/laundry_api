                <h3 style="text-align: left;">4. Mecanismos para ejercer los derechos vinculados a la protección de datos personales (derechos ARCO).</h3>
                <p style="text-align: justify;">
                    A fin de brindar al titular el medio para ejercer los derechos ARCO, <b>Miss Laundry</b> pone a disposición de los titulares la dirección de correo electrónico <b>Miss Laundry</b> contacto@misslaundry.app a
                    través de la cual se dará trámite y seguimiento a las solicitudes de acceso, rectificación, cancelación u oposición.
                    <br />
                    El titular podrá enviar a través de este medio una solicitud para el acceso, rectificación, cancelación u oposición, respecto de sus datos personales.
                    Dicha solicitud deberá contener:
                    <br />
                    <ul>
                        <li>El nombre completo del titular y todos los datos que le fueron solicitados para registrarse así como un correo electrónico para comunicarle la respuesta a su solicitud.</li>
                        <li>Copia electrónica de los documentos que acrediten la identidad del titular de los datos personales (RFC, IFE).</li>
                        <li>Descripción clara y precisa de los datos personales respecto de los que se busca ejercer alguno de los derechos antes mencionados.</li>
                        <li>Cualquier otro elemento o documento que facilite la localización de los datos personales.</li>
                    </ul>
                    <br />
                    Indicar de las modificaciones a realizarse y/o las limitaciones al uso de los datos personales y aportar la documentación que sustente su petición.
                    <br />
                    <b>Miss Laundry</b> comunicará al titular de los datos personales la determinación adoptada, en un plazo no mayor a 20 días hábiles contados desde la fecha en que se recibió la solicitud.
                    Este plazo podrá ser ampliado por <b>Miss Laundry</b> en una sola ocasión por un periodo igual, siempre y cuando así lo justifiquen las circunstancias del caso.
                    <br />
                    <b>Miss Laundry</b> informará al titular de los datos personales el sentido y motivación de la resolución por correo electrónico y acompañará dicha resolución de las pruebas pertinentes, en
                    su caso.
